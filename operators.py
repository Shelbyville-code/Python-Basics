#Arithmetic operators-Simple Calcations

num1=45
num2=20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Modulus-Returns the remainder

#Comparison operators
x=78
y=60

print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)

#Assignment operators-used to assign values to variables
number=40
print(number)

a=35
a-=2
print(a)

result=90-(6*3)/2+5 #Operator Precedence
print(result)

#Logical operators-and,or,not
print(90<40 and 67>34)
print(90<40 or 67>34)
print(not(90<40 or 67>34))